    private void btnFicheroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFicheroActionPerformed
         
        String edad=txtEdad.getText();
        String nombre=txtNombre.getText();
        String telefono=txtTelefono.getText();
        String correo=txtCorreo.getText();
        Date fecha = jcFecha.getDate();
        String razon = cbRazon.getSelectedItem().toString();
       
        
        String MosTex= atxt.getText();
        
        
        
        File fichero = new File ("RegistroCliente.txt");
        
        try {
           
            BufferedWriter fichero2 = new BufferedWriter(new FileWriter(fichero));
           
            fichero2.append("Edad: "+edad);
            fichero2.newLine();
            fichero2.newLine();
            fichero2.append("Nombre: "+nombre);
            fichero2.newLine();
            fichero2.newLine();
            fichero2.append("Telefono: "+telefono);
            fichero2.newLine();
            fichero2.newLine();
            fichero2.append("Correo: "+correo);
            fichero2.newLine();
            fichero2.newLine();
            fichero2.append("Fecha: "+fecha);
            fichero2.newLine();
            fichero2.newLine();
            fichero2.append("Razon de consulta:"+razon);
            fichero2.newLine();
            fichero2.newLine();
            
          
             fichero2.append("Descripcio: ");
            fichero2.newLine();
            fichero2.append(MosTex);
            fichero2.newLine();
           fichero2.close();

           
        } catch (IOException ex) {
            Logger.getLogger(FrmClientes.class.getName()).log(Level.SEVERE, null, ex);
        }
        
         JOptionPane.showMessageDialog(this,"Tus datos se guardaron exitosamente ;) ");  
    }//GEN-LAST:event_btnFicheroActionPerformed

