    private void btnBuascarEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuascarEmpActionPerformed
        // TODO add your handling code here:
        
        String strID = txtIdEmp.getText();
        if(strID.length() == 0){
        strID = (String)JOptionPane.showInputDialog(
                "Debe digitar el numero de identificacion");  
            if (strID == null) 
                return;
        }
        
        int k = 0;
        boolean sw = false;
       int ID = Integer.parseInt(strID); 
       while (k < contador && sw == false) {
       
           emp = vecEmpleado[k];
           if(emp.getID() == ID){
           sw = true;
           }else{
               k++;
           }
       
       }
        
       if(sw == true){
       
       
           txtIdEmp.setText(emp.getID() + "");
           txtNumCarnetEmp.setText(emp.getNumeroCarnet() + "");
           txtNombreEmp.setText(emp.getNombre());
           txtApellidoEmp.setText(emp.getApellido());
            dcNacimientoEmp.setDate(emp.getNacimiento());
            
            cboTipoSangreEmp.setSelectedItem(emp.getTipoSangre() + "");
            cboTipoDocEmp.setSelectedIndex(emp.getTipoID());
            int INDICEGENERO = (emp.getGenero() == 'F' ? 0 : 1);
            cboGeneroEmp.setSelectedIndex( INDICEGENERO );
            txtPesoEmp.setText(emp.getPeso()+ "");
            txtEstaturaEmp.setText(emp.getEstatura() + "");
       }else{
           
            
             JOptionPane.showMessageDialog(this,"No se econtro al empleado");  
           
           
       
       
       } 
       
       
       
        
        
        
    }//GEN-LAST:event_btnBuascarEmpActionPerformed

