    private void btnGuardarEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarEmpActionPerformed
        // TODO add your handling code here:
        if (contador < vecEmpleado.length) {
            
            int ID = Integer.parseInt(txtIdEmp.getText());
            int TIPODOCUMENTO = cboTipoDocEmp.getSelectedIndex();
            int CARNET = Integer.parseInt(txtNumCarnetEmp.getText());
            String NOMBRE = txtNombreEmp.getText();
            String APELLIDOS = txtApellidoEmp.getText();
            Date FECHANACIMIENTO = dcNacimientoEmp.getDate();
            String TIPOSANGRE = cboTipoSangreEmp.getSelectedItem().toString();
            char GENERO = cboGeneroEmp.getSelectedItem().toString().charAt(0);
            double PESO = Double.parseDouble(txtPesoEmp.getText());
            double ESTATURA = Double.parseDouble( txtEstaturaEmp.getText());
             
             
            emp = new Empleado(ID, TIPODOCUMENTO, CARNET, NOMBRE, APELLIDOS, FECHANACIMIENTO, TIPOSANGRE, GENERO, PESO, ESTATURA);
            vecEmpleado[contador] = emp;
            contador ++;
            
            JOptionPane.showMessageDialog(this,"Se ingreso un nuevo empleado");
            
        }else{
          JOptionPane.showMessageDialog(this,"No hay mas lugares para empleados");  
             
            
        }
 
        
    }//GEN-LAST:event_btnGuardarEmpActionPerformed

